clc;
clear all;
close all;
tfinal=0.05;
t=0:0.00005:tfinal;
fd=input('Enter the analog frequency:'); xt=sin(2*pi*t*fd);
fs1=1.3*fd;
n1=0:1/fs1:tfinal;
xn=sin(2*pi*n1*fd);
subplot(3,1,1);
plot(t,xt,'b',n1,xn,'r*-');
title('under sampling');
fs2=2*fd;
n2=0:1/fs2:tfinal;
xn=sin(2*pi*n2*fd);
subplot(3,1,2);
plot(t,xt,'b',n2,xn,'r*-');
title('nyquist rate');
fs3=2.5*fd;
n3=0:1/fs3:tfinal;
xn=sin(2*pi*n3*fd);
subplot(3,1,3);
plot(t,xt,'b',n3,xn,'r*-');
title('over sampling');

f1 = input('Enter the first sine wave frequency = '); f2 = input('Enter the second sine wave frequency = '); fn = 2*max(f1,f2);
fs = fn/2;
t = 0:1/fs:0.1;
x = cos(2*pi*f1*t)+cos(2*pi*f2*t);
xk = fft(x);
f = (0:length(xk)-1)*fs/length(xk);
figure(1);
plot(f,abs(xk));
xlabel('frequency');
ylabel('amplitude');
title('Under Sampling');
grid;
